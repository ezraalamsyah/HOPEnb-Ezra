package Model;

/**
 *
 * @author MikyalN
 */
public class Contact {
    private int idC;
    private String namaC, emailC, subjekC, pesanC;

    /**
     * @return the idC
     */
    public int getIdC() {
        return idC;
    }

    /**
     * @param idC the idC to set
     */
    public void setIdC(int idC) {
        this.idC = idC;
    }

    /**
     * @return the namaC
     */
    public String getNamaC() {
        return namaC;
    }

    /**
     * @param namaC the namaC to set
     */
    public void setNamaC(String namaC) {
        this.namaC = namaC;
    }

    /**
     * @return the emailC
     */
    public String getEmailC() {
        return emailC;
    }

    /**
     * @param emailC the emailC to set
     */
    public void setEmailC(String emailC) {
        this.emailC = emailC;
    }

    /**
     * @return the subjekC
     */
    public String getSubjekC() {
        return subjekC;
    }

    /**
     * @param subjekC the subjekC to set
     */
    public void setSubjekC(String subjekC) {
        this.subjekC = subjekC;
    }

    /**
     * @return the pesanC
     */
    public String getPesanC() {
        return pesanC;
    }

    /**
     * @param pesanC the pesanC to set
     */
    public void setPesanC(String pesanC) {
        this.pesanC = pesanC;
    }
    
}
