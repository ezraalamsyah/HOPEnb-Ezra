package Model;

/**
 *
 * @author MikyalN
 */
public class Donation {
    private int idD, jumlahD;
    private String namaD, emailD, metodeD, pesanD;

    public int getIdD() {
        return idD;
    }

    public void setIdD(int idD) {
        this.idD = idD;
    }

    public int getJumlahD() {
        return jumlahD;
    }

    public void setJumlahD(int jumlahD) {
        this.jumlahD = jumlahD;
    }

    public String getNamaD() {
        return namaD;
    }

    public void setNamaD(String namaD) {
        this.namaD = namaD;
    }

    public String getEmailD() {
        return emailD;
    }

    public void setEmailD(String emailD) {
        this.emailD = emailD;
    }

    public String getMetodeD() {
        return metodeD;
    }

    public void setMetodeD(String metodeD) {
        this.metodeD = metodeD;
    }

    public String getPesanD() {
        return pesanD;
    }

    public void setPesanD(String pesanD) {
        this.pesanD = pesanD;
    } 
}
