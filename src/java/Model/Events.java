package Model;

/**
 *
 * @author MikyalN
 */
public class Events {
    private int idE;
    private String namaE, imageE, altE, lokasiE, captE;

    /**
     * @return the idE
     */
    public int getIdE() {
        return idE;
    }

    /**
     * @param idE the idE to set
     */
    public void setIdE(int idE) {
        this.idE = idE;
    }

    /**
     * @return the namaE
     */
    public String getNamaE() {
        return namaE;
    }

    /**
     * @param namaE the namaE to set
     */
    public void setNamaE(String namaE) {
        this.namaE = namaE;
    }

    /**
     * @return the imageE
     */
    public String getImageE() {
        return imageE;
    }

    /**
     * @param imageE the imageE to set
     */
    public void setImageE(String imageE) {
        this.imageE = imageE;
    }

    /**
     * @return the altE
     */
    public String getAltE() {
        return altE;
    }

    /**
     * @param altE the altE to set
     */
    public void setAltE(String altE) {
        this.altE = altE;
    }

    /**
     * @return the lokasiE
     */
    public String getLokasiE() {
        return lokasiE;
    }

    /**
     * @param lokasiE the lokasiE to set
     */
    public void setLokasiE(String lokasiE) {
        this.lokasiE = lokasiE;
    }

    /**
     * @return the captE
     */
    public String getCaptE() {
        return captE;
    }

    /**
     * @param captE the captE to set
     */
    public void setCaptE(String captE) {
        this.captE = captE;
    }
    
}
